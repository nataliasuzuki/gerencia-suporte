    @Override
    public void consultaObjeto(JTable tabelaSuporte, String tipoSuporte) {
        PreparedStatement ps;
        ResultSet rs;
        
        String selectQuery = "SELECT * FROM `dadossuporte` WHERE `TipoSuporte` LIKE ?";
        
        try {
            ps = mycon.criarConexao().prepareStatement(selectQuery);
            ps.setString(1, tipoSuporte);
            rs = ps.executeQuery();
            DefaultTableModel tabelaModel = (DefaultTableModel) tabelaSuporte.getModel();
            
            Object[] row;
            while(rs.next())
            {
                row = new Object[7];
                row[0] = rs.getInt(1);
                row[1] = rs.getString(2);
                row[2] = rs.getString(3);
                row[3] = rs.getString(4);
                row[4] = rs.getString(5);
                row[5] = rs.getString(6);
                row[6] = rs.getInt(7);
                
                tabelaModel.addRow(row);
            }
                    
        } catch (SQLException ex) {
            Logger.getLogger(Cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
