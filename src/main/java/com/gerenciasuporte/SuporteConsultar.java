    private void popularLinhas(HSSFCellStyle sLinha1, HSSFCellStyle sLinha2, int i, HSSFRow row, Object req, Object cpf, Object cpl, Object inscMunicipla, Object razaoSocial, Object dataAutodeclaracao) {

        //Cidade
        HSSFCell cell0 = row.createCell(0);
        if (req != null){
                cell0.setCellValue(req.toString().toUpperCase());
        }else {
                cell0.setCellValue("");
        }

        //Pais
        HSSFCell cell1 = row.createCell(1);
        if (cpf != null){
                cell1.setCellValue(cpf.toString());
        }else {
                cell1.setCellValue("");
        }	

        //HoraInicio
        HSSFCell cell2 = row.createCell(2);
        if (cpl != null){
                cell2.setCellValue(cpl.toString());
        }
        else {
                cell2.setCellValue("");
        }
        //HoraEncerra
        HSSFCell cell3 = row.createCell(3);
        if (inscMunicipla != null){
                cell3.setCellValue(inscMunicipla.toString());
        }
        else {
                cell3.setCellValue("");
        }
        
        //TipoSuporte
        HSSFCell cell4 = row.createCell(4);
        if (razaoSocial != null){
                cell4.setCellValue(razaoSocial.toString());
        }
        else {
                cell4.setCellValue("");
        }

        //Telefone
        HSSFCell cell5 = row.createCell(5);
        if (dataAutodeclaracao != null){
                String dataFormatada = dataAutodeclaracao.toString();
                cell5.setCellValue(dataFormatada);
        }
        else {
                cell5.setCellValue("");
        }

        if (i % 2 == 0) {
                cell0.setCellStyle(sLinha1);
                cell1.setCellStyle(sLinha1);
                cell2.setCellStyle(sLinha1);
                cell3.setCellStyle(sLinha1);
                cell4.setCellStyle(sLinha1);
                cell5.setCellStyle(sLinha1);
        }
        else {
                cell0.setCellStyle(sLinha2);
                cell1.setCellStyle(sLinha2);
                cell2.setCellStyle(sLinha2);
                cell3.setCellStyle(sLinha2);
                cell4.setCellStyle(sLinha2);
                cell5.setCellStyle(sLinha2);
        }
    }
